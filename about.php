 <h1 class="styled-heading"><u>ABOUT US</u></h1>
    
<div class="fonta"><div class="background-border"><p><i><h4>Understanding the necessity of feeding starving stray animals, especially stray dogs, we started feeding them. It was during the first Covid19 in 2019, we came to realise how pathetic the condition of stray animals who are depended on restaurant leftovers, street shops or some pieces of biscuits people give for them. Human presence along with functioning of canteens, hotels and other shops ensured some food, but they were pushed to starvation, without a single morsel of food, with the shutdown of each and every shop. Also a few animal lovers / feeders that used to feed them had difficulty in going out for feeding. Seeing those literally starving without a single decent meal or water, our feeding programme was started with feeding of 50-60 strays around our areas.
</h4></div></p>

<i>It reached to 250 street animals in December, 2019. As we understood the need of expanding the service, the feeding programme currently feeds above 550 dogs, cats, pigs etc. on their spots with the help volunteer groups and staff. Focusing on the industrial areas of Pondicherry and Nearby Tamil Nadu, everyday street animals are fed with healthy meals once a day. With the help of supporters and animal lovers, we have been able to feed more strays. Volunteers collect and feed strays at different place while the staffs go every night food taken in van to different routes.

Feeding animals is the first step towards befriending them hence to rescue and provide medical treatment in need. It also clears the way to get them for sterilisation and vaccinations.
